#include <iostream>
using namespace std;
int main() {
    int variable1=0, variable2=0;
    cout<<"valor inicial de variable1: "<<variable1<<endl;
    cout<<"valor inicial de variable2: "<<variable2<<endl;
    cout<<"Ingrese el valor de variable1: ";
    cin>>variable1;//ingresamos nuevo valor a variable1
    cout<<"Ingrese el valor de variable2: ";
    cin>>variable2;//ingresamos nuevo valor a variable2
    cout<<"valor actual de variable1: "<<variable1<<endl;
    cout<<"valor actual de variable2: "<<variable2<<endl;
    return 0;
}
//En este programa ya empezamos a declarar variables, en este caso con "int" con la cual declararamos estas
//variables como aptas para numeros enteros, tambien utilizamos "cin" para poder darle un valor a esas
//variables teniendo una estructura similar a cout pero con los simbolos ">>" 

